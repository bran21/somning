export function initEcosystem() {
    const container = document.getElementById('ecosystemListContainer');
    if (!container) return;

    let html = '';

    // Define category emojis to match previous styling
    const categoryIcons = {
        'DeFi': '💰',
        'Gaming': '🎮',
        'Infrastructure': '🔧',
        'NFT': '🖼️',
        'Other dApps': '🌐'
    };

    for (const [category, projects] of Object.entries(ecosystemData)) {
        html += `<div class="sidebar-category">`;
        const icon = categoryIcons[category] || '🔹';
        html += `<div class="category-title">${icon} ${category}</div>`;

        projects.forEach(project => {
            const url = project.website_url || '#';
            const target = url === '#' ? '' : 'target="_blank"';

            // Handle logo, and gracefully fallback to default Somnia logo on error
            let imgHtml = '';
            if (project.image_url) {
                imgHtml = `<img src="${project.image_url}" alt="${project.name}" onerror="this.onerror=null; this.src='https://1813806305-files.gitbook.io/~/files/v0/b/gitbook-x-prod.appspot.com/o/organizations%2FXixzx30CXHthiaKhEu1D%2Fsites%2Fsite_m7x2t%2Ficon%2FjdslfIGTxvTkjBH77O7V%2Flogo.png?alt=media&token=fbc7ca1b-24b9-4847-ad0c-574ac536eff3';">`;
            } else {
                imgHtml = `<span>🌐</span>`;
            }

            html += `
                <a href="${url}" ${target} class="sidebar-item">
                    <span class="si-icon">${imgHtml}</span>
                    <span class="si-name">${project.name}</span>
                    <span class="si-tag">${project.category}</span>
                </a>
            `;
        });

        html += `</div>`;
    }

    container.innerHTML = html;
}
